<b>教师角色划分以及角色分配课程和班级信息：</b><br>
1、教师分为任课教师和班主任两个角色。<br>
2、任课教师所教课程和所带班级在“教育系统设置”中“教师任课信息设置”中进行管理。<br>
3、班主任教师设置是在班级管理里面进行设置。<br>
4、任课教师可以查阅自己所带班级的学生档案信息。<br>
5、班主任教师可以管理自己所带班级的学生档案信息。<br>
6、一个教师即可以是班主任，也可以是任课教师。<br><br>

<br>
<b>教师管理学生的内容：</b><br>
1、教师管理学生档案、学生评语、学生作业、学生考勤等信息。<br>
2、教师管理学生留言，并对留言进行回复。<br>
3、教师管理学生成绩，支持数据批量导入，支持把成绩数据直接发送到家长手机号上面。<br>
4、教师查阅学生交费记录，查阅哪些学生已经交费，哪些还没有。<br>
5、教师管理学生密码，学生在忘记密码时可以进行恢复。<br>
6、教师发布班级通知，学生在登录学生端时可以进行通知查阅。<br>
7、教师发布自己的课件，学生在登录学生端可以自行登录下载课件信息<br>

<br>
