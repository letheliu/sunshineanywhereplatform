<div class=small>
<b>教学计划分类：</b><br>
第一：教学计划编制：根据级别设计所有学期的教学计划，具体元素有：级别、专业、课程、课程数，学分、课程类别等信息。<br><br>
第一：教学计划落实：根据自己学校的情况确定本学期教学计划的详细安排，增加元素：教师、上课班级、上课人数等信息。<br>
<br>

<b>教学计划用途说明：</b><br>
第一：课表排课时使用教学计划，如果不设定教学计划，则会按简单的教师任教信息里面的设置进行处理。<br><br>
第一：给教师计算课酬时使用教学计划，用于计算上课课时数。<br>
