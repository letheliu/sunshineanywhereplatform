<b>在线报名使用说明：</b><br><br>
&nbsp;&nbsp;&nbsp;&nbsp;第一：主要适用于像国家英语四六级,计算机等级考试之类的由第三方组织的证书类考试。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;第二：学校统一发布开始报名的起止日期,然后统计后详细的报名名单信息,交由第三方组织进行安排考试。<br>
<br>

<b>题库卷库使用说明：</b><br><br>
&nbsp;&nbsp;&nbsp;&nbsp;第一：可以由学校统一进行题库建立和卷库建立,主要用于考试类的用途。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;第二：可以由教师统一进行题库建立和卷库建立,主要用于学生的练习测验使用。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;第三：题库卷库建立以后,双方都可以相互共享。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;<font color=red>详细说明：其中创建试题信息时要注意：单选多选判断，至少有两个备选答案。<br>
&nbsp;&nbsp;&nbsp;&nbsp;填空题规则比较特别，新建填空题应把AB两项输入空格即可。<br>
&nbsp;&nbsp;&nbsp;&nbsp;因为考虑到是两个答案填空答案甚至是多个答案填空，其中填空题的正确答案一定要以#分开。如：答案一#答案二#答案三</font><br>

<br>

<b>在线考试使用说明：</b><br><br>
&nbsp;&nbsp;&nbsp;&nbsp;第一：主要适用于针对选修课或证书类考试进行的模拟考试,以及教师自发组织的练习题。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;第二：选修课考试完成以会有成绩,记录入学籍档案;证书类考试划分出及格线用于发放证书。<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;第三：考试的对象为学生,考试可以由学校或教师进行发起组织。<br>
<br>
&nbsp;&nbsp;&nbsp;&nbsp;<font color=red>详细说明：考试信息管理要设定好考试的日期和参加考试班级即可。<br>
&nbsp;&nbsp;&nbsp;&nbsp;<font color=red>教师考试要设定好考试的日期和参加考试人员。<br>

